/// [InputAction] representing various input actions that can be taken within a specific context.
enum InputAction {
  /// [newline]Represents an input action to insert a new line or line break.
  /// Used to indicate moving to a new line or creating a line break in the context.
  newline,

  ///[send] Represents an input action to send or submit the entered content.
  /// Used to signal that the input data should be sent or submitted for processing.
  send,
}
